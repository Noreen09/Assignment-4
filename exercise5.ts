// Exercise 5:  Write a program that determines if a person is eligible to vote based on their age.

let a: number = 15;

if (a >= 18) {
  console.log(
    `Congratulations! You are eligible to vote because you are ${a} years old.`
  );
} else {
  console.log(`I'm sorry, You can't vote because you are ${a} years old.`);
}
